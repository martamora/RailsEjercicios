class Calculadora
  
    def suma valor1, valor2
      @resultado = valor1.to_i + valor2.to_i 

    end
   def resta valor1, valor2
      @resultado = valor1.to_i - valor2.to_i 

    end
    
     def multi valor1, valor2
      @resultado = valor1.to_i * valor2.to_i 

    end
    
     def division valor1, valor2
      @resultado = valor1.to_i / valor2.to_i 

    end
end

    

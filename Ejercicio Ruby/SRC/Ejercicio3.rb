def suma (valor1, valor2)
  valor1 + valor2
  
end

 
 def resta (valor1, valor2)
  valor1 - valor2
end

 
 def multiplicacion (valor1, valor2)
   valor1 * valor2
 end

 
 def division (valor1, valor2)
   valor1 / valor2
 end


def mostrar (valor)
  
    puts valor
end

valor  = suma 4,8
mostrar "El resultado de la suma es #{valor}" 
valor = resta 83, 26
mostrar "El resultado de la resta es #{valor}"
valor = multiplicacion 14, 14
mostrar "El resultado de la multiplicacion es #{valor}" 
valor = division 625, 25
mostrar "El resultado de la division es #{valor}" 







